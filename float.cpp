  if (not checkRoundingModeSp(di))
    return;

  float f1 = fpRegs_.readSingle(di->op1());
  float f2 = fpRegs_.readSingle(di->op2());
  float f3 = -fpRegs_.readSingle(di->op3());

  float res = fusedMultiplyAdd(f1, f2, f3);

  fpRegs_.writeSingle(di->op0(), res);

  updateAccruedFpBits();
  markFsDirty();
