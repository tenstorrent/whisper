template <typename URV>
void
Hart<URV>::execFcvtmod_w_d(const DecodedInst* di)
{
  if (not isRvzfa() or not isDpLegal())
    {
      illegalInst(di);
      return;
    }

  clearSimulatorFpFlags();

  double d1 = fpRegs_.readDouble(di->op1());

  SRV result;
  if (std::isinf(d1) or std::isnan(d1))
    {
      result = 0;
      raiseSimulatorFpFlags(FpFlags::Invalid);
    }
  else
    {
      int    exp = 0;
      // frexp uses (-1, .5] and [.5, 1) for the fraction component
      double frac  = std::frexp(d1, &exp);

      if (exp < 1)
        {
          result = 0;
          if (std::fpclassify(frac) != FP_ZERO)
            raiseSimulatorFpFlags(FpFlags::Inexact);
        }
      else
        {
          bool sign = std::signbit(d1);
          uint64_t result64 = 0;
          if (exp >= (64 + std::numeric_limits<double>::digits))
            result = 0;
          else
            {
              // Clear the sign bit and exponent bits
              frac = std::ldexp(std::fabs(frac),
                                std::numeric_limits<double>::min_exponent);

              int shift = exp - std::numeric_limits<double>::digits;
              result64 = std::bit_cast<uint64_t>(frac);
              if (shift > 0)
                result64 <<= shift;
              else
                result64 >>= -shift;

	      auto result32 = int32_t(result64);
              if (sign)
                result32 = -result32;
	      result = result32;  // Sign extend.
            }

          auto minInt32 = std::bit_cast<uint32_t>(std::numeric_limits<int32_t>::min());
          auto maxInt32 = std::bit_cast<uint32_t>(std::numeric_limits<int32_t>::max());

          bool expOverflow = exp > (1 + std::numeric_limits<int32_t>::digits);

          if (expOverflow or result64 > (sign? minInt32 : maxInt32))
            raiseSimulatorFpFlags(FpFlags::Invalid);
          else if (result != d1)
            raiseSimulatorFpFlags(FpFlags::Inexact);
        }
    }

  intRegs_.write(di->op0(), result);

  updateAccruedFpBits();
}


